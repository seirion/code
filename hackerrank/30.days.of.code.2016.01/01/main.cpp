#include <iostream>

using namespace std;

int main() {
    cout << "Primitive : double\n";  // 5.35
    cout << "Primitive : char\n";    // 'a'
    cout << "Primitive : boolean\n"; // false

    cout << "Primitive : int\n";     // 100
    cout << "Referenced : String\n";  // "I am a code monkey"
    cout << "Primitive : boolean\n";  // true

    cout << "Primitive : double\n";  // 17.3
    cout << "Primitive : char\n";  // 'c'
    cout << "Referenced : String\n";  // "derp"
    return 0;
}
